public class Estrategia{

int idestrategia;
String nombre;
String descripcion;
String nro;
int idareaunidad;
int idperspectiva;
int idfilial;

    public int getIdfilial() {
        return idfilial;
    }

    public void setIdfilial(int idfilial) {
        this.idfilial = idfilial;
    }



    public int getIdareaunidad() {
        return idareaunidad;
    }

    public void setIdareaunidad(int idareaunidad) {
        this.idareaunidad = idareaunidad;
    }

    public int getIdperspectiva() {
        return idperspectiva;
    }

    public void setIdperspectiva(int idperspectiva) {
        this.idperspectiva = idperspectiva;
    }






    public int getIdestrategia() {
        return idestrategia;
    }

    public void setIdestrategia(int idestrategia) {
        this.idestrategia = idestrategia;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getNro() {
        return nro;
    }

    public void setNro(String nro) {
        this.nro = nro;
    }

    
        
}
